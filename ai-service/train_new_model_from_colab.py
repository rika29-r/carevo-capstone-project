def recommend_career(

    combined_profile,

    education,

    certification,

    cgpa,

    top_n=5
):

    text = (

        f"{combined_profile} "

        f"{education} "

        f"{certification} "

        f"{cgpa}"
    )

    text = clean_text(
        text
    )

    prediction = model(
        tf.constant([text]),
        training=False
    ).numpy()

    class_idx = np.argmax(
        prediction
    )

    confidence = float(
        np.max(prediction)
    )

    category = (

        label_encoder
        .inverse_transform(
            [class_idx]
        )[0]
    )

    jobs = (
        career_jobs
        .get(
            category,
            []
        )[:top_n]
    )

    return (
        category,
        confidence,
        jobs
    )

