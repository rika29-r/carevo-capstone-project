@tf.keras.utils.register_keras_serializable()
class AttentionLayer(
    tf.keras.layers.Layer
):

    def build(
        self,
        input_shape
    ):

        self.W = self.add_weight(

            shape=(
                input_shape[-1],
                1
            ),

            initializer="random_normal",

            trainable=True
        )

    def call(
        self,
        inputs
    ):

        score = tf.matmul(
            inputs,
            self.W
        )

        weights = tf.nn.softmax(
            score,
            axis=1
        )

        context = (
            weights *
            inputs
        )

        context = tf.reduce_sum(
            context,
            axis=1
        )

        return context

class AccuracyLogger(
    Callback
):

    def on_epoch_end(
        self,
        epoch,
        logs=None
    ):

        print(
            f"Epoch {epoch+1} | "
            f"Accuracy={logs['accuracy']:.4f}"
        )

EMBEDDING_DIM = 64

@tf.keras.utils.register_keras_serializable()
class CareerModel(
    tf.keras.Model
):

    def __init__(self):

        super().__init__()

        self.vectorizer = text_vectorizer

        self.embedding = layers.Embedding(

            MAX_TOKENS,

            EMBEDDING_DIM,

            mask_zero=True
        )

        self.bigru = layers.Bidirectional(

            layers.GRU(

                64,

                return_sequences=True,

                dropout=0.3
            )
        )

        self.attention = AttentionLayer()

        self.dense = layers.Dense(

            128,

            activation="relu"
        )

        self.dropout = layers.Dropout(
            0.5
        )

        self.output_layer = layers.Dense(

            num_classes,

            activation="softmax"
        )

    def call(
        self,
        inputs
    ):

        x = self.vectorizer(inputs)

        x = self.embedding(x)

        x = self.bigru(x)

        x = self.attention(x)

        x = self.dense(x)

        x = self.dropout(x)

        return self.output_layer(x)

